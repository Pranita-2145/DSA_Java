package DSA_Java;
import java.util.*;
public class Bitwise {
    public static void main(String[] args) {
        System.out.println((5 & 6)); // AND
        System.out.println((5 | 6)); //OR
        System.out.println((5 ^ 6)); //XOR
        System.out.println((~5)); //NOT
        System.out.println((5<<3));// Bitwise left shift
    }
}
